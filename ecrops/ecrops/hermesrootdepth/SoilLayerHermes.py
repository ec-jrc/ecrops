class SoilLayerHermes():
    """ This class represents the soil layer in an Hermes nitrogen component"""

    upperBoundary = float(0)
    """Upper boundary of the layer (cm)"""

    lowerBoundary = float(0)
    """Lower boundary of the layer (cm)"""

    cumulativePercentageOfRootUntilLayer = float(0)
    """Cumulative percentage of roots until this layer """

    percentageOfRootInLayer = float(0)
    """Percentage of roots in this layer"""
